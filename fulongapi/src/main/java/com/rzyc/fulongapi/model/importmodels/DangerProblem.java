package com.rzyc.fulongapi.model.importmodels;

public class DangerProblem {

    private String dangerProblemName;

    private String imageOfRectificationBefore;

    private String imageOfRectificationAfter;

    public String getDangerProblemName() {
        return dangerProblemName;
    }

    public void setDangerProblemName(String dangerProblemName) {
        this.dangerProblemName = dangerProblemName;
    }

    public String getImageOfRectificationBefore() {
        return imageOfRectificationBefore;
    }

    public void setImageOfRectificationBefore(String imageOfRectificationBefore) {
        this.imageOfRectificationBefore = imageOfRectificationBefore;
    }

    public String getImageOfRectificationAfter() {
        return imageOfRectificationAfter;
    }

    public void setImageOfRectificationAfter(String imageOfRectificationAfter) {
        this.imageOfRectificationAfter = imageOfRectificationAfter;
    }
}
